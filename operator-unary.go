package main

import "fmt"

func main() {

	// UNARY OPERATOR

	// OPERATOR			KETERANGAN
	// ++									a = a + 1
	// --									a = a - 1
	// -									 Negative
	// +									 Positive
	// !									 Boolean kebalikan
	var a = 1
	var b = 1
	fmt.Println(a)
	fmt.Println(b)

	a++
	b--
	fmt.Println(a)
	fmt.Println(b)

	a++
	b--
	fmt.Println(a)
	fmt.Println(b)
}
